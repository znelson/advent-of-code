class LightGrid:
	def __init__(self, food, hot_corners=False):
		self.grid = []
		self.hot_corners = hot_corners
		self.eat(food)

	def eat(self, food):
		lines = food.split('\n')
		for line in lines:
			self.grid.append([(item == '#') for item in line])

		self._update_corners()

	def _update_corners(self):
		if self.hot_corners:
			self.grid[0][0] = True
			self.grid[0][len(self.grid[0]) - 1] = True
			self.grid[len(self.grid) - 1][0] = True
			self.grid[len(self.grid) - 1][len(self.grid[0]) - 1] = True

	def _one_step(self):
		newgrid = []
		for i, row in enumerate(self.grid):

			newrow = []
			for j, item in enumerate(row):

				buddies = []

				top_edge = (i == 0)
				bottom_edge = (i == (len(self.grid) - 1))
				left_edge = (j == 0)
				right_edge = (j == (len(row) - 1))

				if not top_edge:
					if not left_edge:
						buddies.append(self.grid[i-1][j-1])
					else:
						buddies.append(False)

					buddies.append(self.grid[i-1][j])

					if not right_edge:
						buddies.append(self.grid[i-1][j+1])
					else:
						buddies.append(False)
				else:
					buddies.extend([False] * 3)

				if not left_edge:
					buddies.append(self.grid[i][j-1])
				else:
					buddies.append(False)

				if not right_edge:
					buddies.append(self.grid[i][j+1])
				else:
					buddies.append(False)

				if not bottom_edge:
					if not left_edge:
						buddies.append(self.grid[i+1][j-1])
					else:
						buddies.append(False)

					buddies.append(self.grid[i+1][j])

					if not right_edge:
						buddies.append(self.grid[i+1][j+1])
					else:
						buddies.append(False)
				else:
					buddies.extend([False] * 3)

				newitem = item
				buddy_sum = sum(buddies)
				if item and buddy_sum not in [2, 3]:
					newitem = False
				elif not item and buddy_sum == 3:
					newitem = True

				newrow.append(newitem)
			newgrid.append(newrow)

		self.grid = newgrid

		self._update_corners()

	def step(self, count=1):
		for i in range(count):
			self._one_step()

	def __repr__(self):
		s = ''
		for row in self.grid:
			for item in row:
				if item:
					s += '#'
				else:
					s += '.'
			s += os.linesep
		return s[:-1]

	def count_on(self):
		count = 0
		for row in self.grid:
			count += sum(row)
		return count

lg = LightGrid(data)
